package com.elitecore.sm.device.model;

/**
 * @author Ranjitsinh Reval
 *
 */
public class SearchDeviceMapping {

	private String deviceTypeName;
	private String vendorTypeName;
	private String deviceName;
	private String decodeType;
	private String mappingName;
	
	
	/**
	 * @return the deviceTypeName
	 */
	public String getDeviceTypeName() {
		return deviceTypeName;
	}
	/**
	 * @param deviceTypeName the deviceTypeName to set
	 */
	public void setDeviceTypeName(String deviceTypeName) {
		this.deviceTypeName = deviceTypeName;
	}
	/**
	 * @return the vendorTypeName
	 */
	public String getVendorTypeName() {
		return vendorTypeName;
	}
	/**
	 * @param vendorTypeName the vendorTypeName to set
	 */
	public void setVendorTypeName(String vendorTypeName) {
		this.vendorTypeName = vendorTypeName;
	}

	/**
	 * @return the decodeType
	 */
	public String getDecodeType() {
		return decodeType;
	}
	/**
	 * @param decodeType the decodeType to set
	 */
	public void setDecodeType(String decodeType) {
		this.decodeType = decodeType;
	}
	/**
	 * @return the mappingName
	 */
	public String getMappingName() {
		return mappingName;
	}
	/**
	 * @param mappingName the mappingName to set
	 */
	public void setMappingName(String mappingName) {
		this.mappingName = mappingName;
	}
	/**
	 * @return the deviceName
	 */
	public String getDeviceName() {
		return deviceName;
	}
	/**
	 * @param deviceName the deviceName to set
	 */
	public void setDeviceName(String deviceName) {
		this.deviceName = deviceName;
	}

	
}
